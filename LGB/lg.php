            <h3>Porque é uma língua</h3>
            <p>A expressão "língua gestual", ao invés de "linguagem gestual", refere-se à língua materna de uma comunidade de surdos. As línguas gestuais são línguas naturais, que surgem e
              se desenvolvem naturalmente, como as línguas orais. Esta língua é produzida por movimentos das mãos, do corpo e por expressões faciais e a sua recepção é visual.
              Tem um vocabulário e gramática próprios.</p>

            <p>Uma língua é um sistema de comunicação específico e exclusivo do ser humano, sendo gerido por regras particulares. Assim como as línguas orais, a LGP possui as características das línguas naturais:</p>
            <ul>
              <li>é composta, maioritariamente por símbolos arbitrários</li>
              <li>é um sistema linguístico</li>
              <li>é partilhada por uma comunidade de pessoas que a utilizam como sua forma de expressão mais natural</li>
              <li>possui propriedades como a criatividade e a recursividade</li>
              <li>possui aspectos contrastivos</li>
              <li>é um sistema em constante renovação e evolução: apresenta o fenómeno da dinâmica linguística</li>
            </ul>

            <p>Existe a crença de que língua gestual, é universal. Essa ideia é incorreta. Assim como as línguas orais, as línguas gestuais desenvolveram-se naturalmente, e assim sendo,
              cada comunidade possui a sua. Existem países com diversas línguas gestuais e em todo o mundo existem dezenas de línguas gestuais diferentes. Além disso, os surdos sentem
              as mesmas dificuldades que os ouvintes quando necessitam comunicar com outros que utilizam uma língua gestual diferente.[3] Assim sendo, cada país (e, por vezes, região
              dentro de um país, como acontece no Québec onde é usada a Língua Gestual Quebequiana ao invés da Língua Gestual Americana como no resto do país) terá a sua própria língua
              gestual. Por exemplo, no Brasil existe a Língua Brasileira de Sinais (LIBRAS).</p>

          <p>Para acrescentar, como qualquer língua oral, a LGP possui variantes dentro do seu próprio órgão (idioma), alterando, relativamente, de região para região e dependendo
            do grau de instrução e das profissões dos surdos, em cada uma das regiões. Existem, por isso, dialetos e regionalismos.</p>

            <h3>História</h3>
            <p>No século XIX, o rei D. João VI chamou a Portugal Pär Aron Borg, um sueco que tinha fundado no seu país um instituto para educação de surdos e logo em 1823 é criada
              a primeira escola portuguesa com essa valência. Assim embora o vocabulário da LGPortuguesa e da LGSueca seja diferente, o alfabeto das duas línguas revela a sua origem comum.</p>

              <p>Nos termos da alínea h) do n.º 2 do artigo 74.º da Constituição da República Portuguesa, «na realização da política de ensino incumbe ao Estado (...) proteger e valorizar
                a língua gestual portuguesa, enquanto expressão cultural e instrumento de acesso à educação e da igualdade de oportunidades». Deste modo, desde 1997, a Língua Gestual Portuguesa
                passou a ser uma das línguas oficiais de Portugal, junto com a Língua Portuguesa e o Mirandês.</p>

            <p>O Dia Nacional da Língua Gestual Portuguesa comemora-se a <b>15 de Novembro</b>. A comemoração deste dia originou-se dos esforços da Comissão Para o Reconhecimento e Protecção
              da Língua Gestual Portuguesa e Defesa dos Direitos Das Pessoas Surdas.</p>


